from typing import Tuple


class Color(object):
    """Class with BGR values for different colors"""

    RED: Tuple = (0, 0, 255)
    GREEN: Tuple = (0, 255, 0)
    BLUE: Tuple = (255, 0, 0)
    PURPLE: Tuple = (255, 0, 255)
    YELLOW: Tuple = (0, 255, 255)
    ORANGE: Tuple = (0, 128, 255)
    WHITE: Tuple = (255, 255, 255)
    BLACK: Tuple = (0, 0, 0)
